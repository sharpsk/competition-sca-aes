    def shift_rows(self, state: np.ndarray) -> np.ndarray:
        """
        AES ShiftRows operation.
        
        Args:
            state: 4x4 state matrix
        """
        if state.shape != (4, 4):
            raise ValueError("State must be 4x4 matrix")
        
        result = state.copy()
        # Shift second row by 1
        result[1] = np.roll(state[1], -1)
        # Shift third row by 2  
        result[2] = np.roll(state[2], -2)
        # Shift fourth row by 3
        result[3] = np.roll(state[3], -3)
        
        return result
    
